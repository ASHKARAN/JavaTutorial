package ir.porteqali.javatutorial.P11_SimpleProject;

public class E057_Calculator {


    public int sum(int a , int b) {
        return  a+b;
    }

    public int minus(int a , int b) {
        return  a-b;
    }

    public int div(int a , int b) {
        return  a/b;
    }

    public int mult(int a , int b) {
        return  a*b;
    }


}
