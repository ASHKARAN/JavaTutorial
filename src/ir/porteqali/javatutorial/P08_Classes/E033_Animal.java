package ir.porteqali.javatutorial.P08_Classes;


public class E033_Animal {

    public int age =   20 ;
    public int height = 120;
    public int foots = 4 ;

    public void walk() {
        System.out.println("i'm walking");
    }

    public void breath() {
        System.out.println("i'm breathing");
    }


    public void beat() {
        System.out.println("my heart is beating");
    }


    public int getAge() {
        return  age;
    }

    public int getHeight() {
        return  height;
    }

    public int getFoots() {
        return  foots;
    }


}
